#include <stdio.h>
#include <string.h>
#include "md5.h"
// #include "md5_2.h"

unsigned  char data[]={0x01,0x01,0x02,0x03,0x04,0x05,0x06,0x07,0x08,0x09,0x0a,0x0b,0x0c,0x0d,0x0e,0x0f,\
						0x01,0x01,0x02,0x03,0x04,0x05,0x06,0x07,0x08,0x09,0x0a,0x0b,0x0c,0x0d,0x0e,0x0f}; 
int main()
{
	int len = sizeof(data);
	printf("len:%d\n",len );
	unsigned char digest[16] = {0};
	MD5_CTX ctx;
	MD5Init(&ctx);
	MD5Update(&ctx, (unsigned char*)data, len);
	MD5Final(digest, &ctx);
	for(int i;i<16;i++)
	{
		printf("%02x ", digest[i]);
	}
	printf("\n");
	return 0;
}